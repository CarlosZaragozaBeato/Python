
#ejemplo para break
print("while con la sentencia break\n")
contador = 0
while contador <10:
    contador +=1
    if(contador ==5):
        break
    print("Valor actual del contador: ",contador)
print("final del programa.")


#ejemplo para continue
print("\n While con la sentence continue\n")
contador = 0
while contador <10:
    contador +=1
    if(contador == 5):
        continue

    print("Valor actual del contador", contador)
print("Final del programa")
